package com.mycompany.jsouptutorial;


/**
 *
 * @author prashant
 */
public class StockParam {

    private String nameSymbol;
    private String address;
    private String email;
    private String website;
    private String tradedPrice;
    private String charge;
    private String listedShare;
    private String paidUpValue;
    private String totalPaidUpValue;
    private String marketPrice;
    private String marketCapitalization;

    public String getNameSymbol() {
        return nameSymbol;
    }

    public void setNameSymbol(String nameSymbol) {
        this.nameSymbol = nameSymbol;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getTradedPrice() {
        return tradedPrice;
    }

    public void setTradedPrice(String tradedPrice) {
        this.tradedPrice = tradedPrice;
    }

    public String getCharge() {
        return charge;
    }

    public void setCharge(String charge) {
        this.charge = charge;
    }

    public String getListedShare() {
        return listedShare;
    }

    public void setListedShare(String listedShare) {
        this.listedShare = listedShare;
    }

    public String getPaidUpValue() {
        return paidUpValue;
    }

    public void setPaidUpValue(String paidUpValue) {
        this.paidUpValue = paidUpValue;
    }

    public String getTotalPaidUpValue() {
        return totalPaidUpValue;
    }

    public void setTotalPaidUpValue(String totalPaidUpValue) {
        this.totalPaidUpValue = totalPaidUpValue;
    }

    public String getMarketPrice() {
        return marketPrice;
    }

    public void setMarketPrice(String marketPrice) {
        this.marketPrice = marketPrice;
    }

    public String getMarketCapitalization() {
        return marketCapitalization;
    }

    public void setMarketCapitalization(String marketCapitalization) {
        this.marketCapitalization = marketCapitalization;
    }

}
